def main():
   dict1 = {'satu': 10, 'dua': 20, 'tiga': 30}

   # membuat salinan dari dict1
   dict2 = dict1.copy()
   print("dict1: ", dict1)
   print("dict2: ", dict2)

if __name__ == "__main__":
   main()
