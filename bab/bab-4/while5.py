def main():
   # menentukan banyak pengulangan
   n = 5
   # melakukan pengulangan
   i = n
   while i >= 1:
      print(i)
      i = i - 1  # decrement, bisa ditulis: i -= 1

if __name__ == "__main__":
   main()
