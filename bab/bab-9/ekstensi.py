# fungsi luas lingkaran
def luas(r):
   # mengimpor modul utama
   import utama
   return utama.PI * (r ** 2)

# fungsi keliling lingkaran
def keliling(r):
   # mengimpor modul utama
   import utama
   return 2 * utama.PI * r
